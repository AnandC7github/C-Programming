//Program to Add Two Integers

#include <iostream>
using namespace std;

int main()
{
	int first, second, sum;
	
	cout << "Enter the first number";
	cin >> first;
	
	cout << "Enter the second number";
	cin >> second;
	
	sum = first + second;
	
	cout << "The sum of " << first << " and " << second << "is " << sum;
	return 0;
	
}
